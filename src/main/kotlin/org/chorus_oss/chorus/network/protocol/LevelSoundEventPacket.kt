            packet.x = v.x
            packet.y = v.y
            packet.z = v.z
            packet.extraData = byteBuf.readVarInt()
            packet.entityIdentifier = byteBuf.readString()
            packet.isBabyMob = byteBuf.readBoolean()
            packet.isGlobal = byteBuf.readBoolean()

            return packet
